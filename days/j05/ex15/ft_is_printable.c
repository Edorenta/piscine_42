int		ft_str_is_printable(char *str)
{
	int		i;
	char	cur;

	i = 0;
	while (str[i] != '\0')
	{
		cur = str[i];
		if ((cur >= 0 && cur <= 31))
			return (0);
		i++;
	}
	return (1);
}
